export function termConverter(termID) {
  if (termID === '1') {
    return 'First Term';
  } else if (termID === '2') {
    return 'Second Term';
  } else if (termID ==='3') {
    return 'Third Term';
  } else if (termID === '4') {
    return 'Fourth Term';
  } else if (termID === '5') {
    return 'Fifth Term';
  } else if (termID === '6') {
    return 'Sixth Term';
  } else if (termID === '7') {
    return 'Seventh Term';
  } else if (termID === '8') {
    return 'Eighth Term';
  } else if (termID === '9') {
    return 'Ninth Term';
  } else if (termID === '10') {
    return 'Tenth Term';
  } else if (termID === '11') {
    return 'Eleventh Term';
  } else if (termID === '12') {
    return 'Twelfth Term';
  } else if (termID === '13') {
    return 'Preparatory Year First Semester';
  } else if (termID === '14') {
    return 'Preparatory Year Second Semester';
  }
}
